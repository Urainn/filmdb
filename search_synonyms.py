def normalize_synonym_groups(raw) -> list[list[str]]:
    """Validate and dedupe synonym groups from JSON."""
    if not isinstance(raw, list):
        return [list(g) for g in DEFAULT_SEARCH_SYNONYM_GROUPS]
    out: list[list[str]] = []
    seen_global: set[str] = set()
    for group in raw:
        if not isinstance(group, list):
            continue
        terms: list[str] = []
        for item in group:
            term = str(item).strip()
            if not term or term in seen_global:
                continue
            seen_global.add(term)
            terms.append(term)
        if len(terms) >= 2:
            out.append(terms)
    return out or [list(g) for g in DEFAULT_SEARCH_SYNONYM_GROUPS]


def build_synonym_map(groups: list[list[str]]) -> dict[str, set[str]]:
    """term -> all synonyms in its group (including itself), lowercased keys."""
    mapping: dict[str, set[str]] = {}
    for group in groups:
        lowered = [t.strip().lower() for t in group if str(t).strip()]
        if len(lowered) < 2:
            continue
        bucket = set(lowered)
        for term in lowered:
            mapping[term] = bucket
    return mapping


def expand_search_terms(query: str, synonym_map: dict[str, set[str]]) -> list[list[str]]:
    """
    Split query by whitespace; each part expands to synonym list.
    Returns list of OR-groups for AND matching.
    """
    parts = [p.strip().lower() for p in (query or "").split() if p.strip()]
    if not parts:
        return []
    expanded: list[list[str]] = []
    for part in parts:
        expanded.append(list(synonym_map.get(part, {part})))
    return expanded


def text_matches_expanded_query(text: str, expanded: list[list[str]]) -> bool:
    if not expanded:
        return True
    haystack = (text or "").lower()
    return all(any(term in haystack for term in group) for group in expanded)
