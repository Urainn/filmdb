def normalize_synonym_groups(raw) -> list[list[str]]:
    """Validate and dedupe synonym groups from JSON."""
    if not isinstance(raw, list):
        return [list(g) for g in DEFAULT_SEARCH_SYNONYM_GROUPS]
    out: list[list[str]] = []
    seen_global: set[str] = set()
    for group in raw:
        if not isinstance(group, list):
            continue
        terms: list[str] = []
        for item in group:
            term = str(item).strip()
            if not term or term in seen_global:
                continue
            seen_global.add(term)
            terms.append(term)
        if len(terms) >= 2:
            out.append(terms)
    return out or [list(g) for g in DEFAULT_SEARCH_SYNONYM_GROUPS]


def build_synonym_map(groups: list[list[str]]) -> dict[str, set[str]]:
    """term -> all synonyms in its group (including itself), lowercased keys."""
    mapping: dict[str, set[str]] = {}
    for group in groups:
        lowered = [t.strip().lower() for t in group if str(t).strip()]
        if len(lowered) < 2:
            continue
        bucket = set(lowered)
        for term in lowered:
            mapping[term] = bucket
    return mapping


def term_matches_text(text: str, term: str) -> bool:
    """Match term in text; short ASCII tokens require word boundaries."""
    import re

    t = (term or "").strip().lower()
    if not t:
        return False
    haystack = (text or "").lower()
    if re.fullmatch(r"[a-z0-9]+", t):
        if len(t) <= 3:
            return re.search(rf"(?:^|[^a-z0-9]){re.escape(t)}(?:[^a-z0-9]|$)", haystack) is not None
        return t in haystack
    if len(t) < 2:
        return False
    return t in haystack


def term_matches_tags(tags: list[str], term: str) -> bool:
    t = (term or "").strip().lower()
    if not t or len(t) < 2:
        return False
    for tag in tags:
        tag_l = str(tag).strip().lower()
        if not tag_l:
            continue
        if tag_l == t or t in tag_l:
            return True
    return False


def movie_search_tags(movie: dict) -> list[str]:
    tags: list[str] = []
    for key in ("genres", "emotions", "atmospheres", "moods", "scenes", "scenesMain", "scenesSub"):
        val = movie.get(key) or []
        if isinstance(val, list):
            tags.extend(str(x).strip() for x in val if str(x).strip())
        elif val:
            tags.append(str(val).strip())
    return tags


def movie_matches_search_term(movie: dict, term: str) -> bool:
    if term_matches_tags(movie_search_tags(movie), term):
        return True
    parts = [
        movie.get("title") or "",
        str(movie.get("year") or ""),
        movie.get("desc") or "",
        movie.get("actors") or movie.get("cast") or "",
    ]
    if isinstance(parts[-1], list):
        parts[-1] = " ".join(str(x) for x in parts[-1])
    return term_matches_text(" ".join(str(p) for p in parts if p), term)


def expand_search_terms(query: str, synonym_map: dict[str, set[str]]) -> list[list[str]]:
    """
    Split query by whitespace; each part expands to synonym list.
    Returns list of OR-groups for AND matching.
    """
    parts = [p.strip().lower() for p in (query or "").split() if p.strip()]
    if not parts:
        return []
    expanded: list[list[str]] = []
    for part in parts:
        expanded.append(list(synonym_map.get(part, {part})))
    return expanded


def text_matches_expanded_query(text: str, expanded: list[list[str]]) -> bool:
    if not expanded:
        return True
    haystack = (text or "").lower()
    return all(any(term_matches_text(haystack, term) for term in group) for group in expanded)


def movie_matches_expanded_query(movie: dict, expanded: list[list[str]]) -> bool:
    if not expanded:
        return True
    return all(
        any(movie_matches_search_term(movie, term) for term in group)
        for group in expanded
    )
